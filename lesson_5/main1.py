# Operações aritiméticas

# Soma
friends = 5
# friends = friends + 1 # forma mais extensa
friends += 1 # forma mais curta
print(f"# Soma - Eu tinha 5 amigos, e agora tenho {friends} amigos.")

# Subtração
friends = 5
# friends = friends - 1 # forma mais extensa
friends -= -1 # forma mais curta
print(f"# Subtração - Eu tinha 5 amigos, e agora tenho {friends} amigos.")

# Multiplicação
friends = 5
# friends = friends * 2 # forma mais extensa
friends *= 2 # forma mais curta
print(f"# Multiplicação - Eu tinha 5 amigos, e agora tenho {friends} amigos.")

# Divisão
friends = 5
# friends = friends / 2 # forma mais extensa
friends /= 2 # forma mais curta
print(f"# Divisão - Eu tinha 5 amigos, e agora tenho {friends} amigos.")

# Exponenciação
friends = 5
# friends = friends ** 2 # forma mais extensa
friends **= 2 # forma mais curta
print(f"# Exponenciação - Eu tinha 5 amigos, e agora tenho {friends} amigos.")

# Módulo
friends = 10
# friends = friends % 2 # forma mais extensa
friends %= 3 # forma mais curta
print(f"# Módulo - Eu tinha 10 amigos, e agora tenho {friends} amigos.")